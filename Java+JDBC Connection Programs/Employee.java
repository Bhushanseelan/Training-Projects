package collectionexample;

import java.util.Date;

public class Employee {
    int id;
    String ename;
    float sal;
    Date doj;
    Date dob;
    String des;
    String dept;
    String address;
    String photo;

    // Getter methods
    public int getId() {
        return id;
    }

    public String getEname() {
        return ename;
    }

    public float getSal() {
        return sal;
    }

    public Date getDoj() {
        return doj;
    }

    public Date getDob() {
        return dob;
    }

    public String getDes() {
        return des;
    }

    public String getDept() {
        return dept;
    }

    public String getAddress() {
        return address;
    }

    public String getPhoto() {
        return photo;
    }
}
